    function changeLanguage(lang) {
        if (!lang) {
            console.error("Language is not defined");
            return;
        }
        const elements = document.querySelectorAll("[data-i18n]");
        if (elements.length === 0) {
            console.error("No elements with data-i18n attribute found");
            return;
        }
        if (!translations[lang]) {
            console.error(`Translations for language "${lang}" not found`);
            return;
        }
        elements.forEach((element => {
            const key = element.getAttribute("data-i18n");
            if (translations[lang][key]) element.innerHTML = translations[lang][key]; else console.warn(`Translation key "${key}" for language "${lang}" not found`);
        }));
        document.querySelectorAll(".lang__btn").forEach((btn => {
            btn.classList.remove("lang__btn_active");
            btn.setAttribute("aria-pressed", "false");
        }));
        const activeButton = document.querySelector(`.lang__btn[data-lang="${lang}"]`);
        if (activeButton) {
            activeButton.classList.add("lang__btn_active");
            activeButton.setAttribute("aria-pressed", "true");
        }
        localStorage.setItem("language", lang);
    }
    document.querySelectorAll(".lang__btn").forEach((btn => {
        btn.addEventListener("click", (() => {
            const lang = btn.getAttribute("data-lang");
            if (!lang) {
                console.error("data-lang attribute is missing");
                return;
            }
            changeLanguage(lang);
        }));
    }));
